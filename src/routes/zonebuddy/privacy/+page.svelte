		<h2>Overview</h2>
		<p>
			ZoneBuddy is designed with your privacy in mind. The app does not collect, transmit, or share
			any personal data. There are no user accounts, no analytics, and no third-party tracking.
		</p>

		<h2>Data Storage</h2>
		<p>
			All data you create in ZoneBuddy (such as workouts and power zone settings) is stored locally
			on your device. If you are signed in to iCloud on your device, this data may also be synced to
			your private iCloud account using Apple's CloudKit service. This sync is handled entirely by
			Apple and is subject to
			<a href="https://www.apple.com/legal/privacy/">Apple's Privacy Policy</a>. ZoneBuddy's
			developer does not have access to your iCloud data.
		</p>

		<h2>Data Collection</h2>
		<p>
			ZoneBuddy does not collect any personal information, usage data, or analytics. No data is sent
			to the developer or any third party.
		</p>

		<h2>Third-Party Services</h2>
		<p>
			ZoneBuddy does not integrate with any third-party analytics, advertising, or tracking
			services.
		</p>

		<h2>Children's Privacy</h2>
		<p>
			ZoneBuddy does not collect any personal information from anyone, including children under the
			age of 13.
		</p>

		<h2>Changes to This Policy</h2>
		<p>
			If this privacy policy changes, the updated policy will be posted on this page with a revised
			date.
		</p>

